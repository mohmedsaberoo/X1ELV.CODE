export const generateCurriculum = (): Chapter[] => {
  return chapterTitles.map((title, idx) => ({
    id: idx + 1,
    title,
    description: `في هذا الفصل سنتعلم ${title} بالتفصيل الممل والسهل.`,
    learningGoals: [
      `فهم أساسيات ${title}`,
      `تطبيق ${title} في مشاريع حقيقية`,
      `حل المشكلات المتعلقة بـ ${title}`
    ],
    isCompleted: false,
    isLocked: idx !== 0,
    imageUrl: `https://picsum.photos/seed/${idx + 100}/800/400`,
    lessons: lessonSyllabusNames.map((lTitle, lIdx) => ({
      id: `${idx + 1}-${lIdx + 1}`,
      chapterId: idx + 1,
      lessonNumber: lIdx + 1,
      title: `${lTitle}: ${title}`,
      isCompleted: false,
      isLocked: !(idx === 0 && lIdx === 0)
    }))
  }));
};
